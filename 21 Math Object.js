// PI

console.log(Math.PI);

// ROUND()

console.log(Math.round(10.32));
console.log(Math.round(97.51));

// POWER()

console.log(Math.pow(2,3));
console.log(2 ** 3);

// SQUARE_ROOT()

console.log(Math.sqrt(64));
console.log(Math.sqrt(81));
console.log(Math.sqrt(88));

// ABSOLUTE()

console.log(Math.abs(-55));
console.log(Math.abs(-55.5));
console.log(Math.abs(-955));
console.log(Math.abs(4-6));

// CEIL()

console.log(Math.ceil(4.51));
console.log(Math.ceil(99.1));

// FLOOR()

console.log(Math.floor(4.51));
console.log(Math.floor(99.1));

// MINIMUM()

console.log(Math.min(0,150,300,-200,-8,20,30));

// MAXIMUM()

console.log(Math.max(0,150,300,-200,-8,20,30));

// RANDOM()

console.log(Math.random());

console.log(Math.random()*10);

console.log(Math.floor(Math.random()*10)); // 0 to 9

// TRUNCATE()

console.log(Math.trunc(4.56));
console.log(Math.trunc(-99.01));

// If the arg is +ve, Math.trunc() is equivalent to Math.floor()
// If the arg is -ve, Math.trunc() is equivalent to Math.ceil()